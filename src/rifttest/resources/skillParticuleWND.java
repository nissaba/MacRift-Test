    public void setData(Element elem, int _type, SkillHandler H) {
        e = elem;
        type = _type;
        handler = H;
        switch (type) {
            case 0:
                // set lables
                tag1.setText("Name");
                tag2.setText("Base skill level");
                tag3.setText("on level up");
                tag4.setText("Not Used");
                box4.setEnabled(false);
                // fill boxs
                box1.setText(elem.getAttribute("name"));
                box2.setText(elem.getAttribute("base"));
                box3.setText(elem.getAttribute("onLevel"));
                break;
            case 1:
                tag1.setText("Name");
                tag2.setText("Bonus");
                tag3.setText("Alternative");
                tag4.setText("Bonus");
                box1.setText(elem.getAttribute("name"));
                box2.setText(elem.getAttribute("bonus"));
                box3.setText(elem.getAttribute("altSkill"));
                box4.setText(elem.getAttribute("altBonus"));
                break;
            case 2:
                tag1.setText("Targeted stat");
                tag2.setText("buns amount");
                tag3.setText("or Die roll");
                tag4.setText("Not Used");
                box4.setEnabled(false);
                box1.setText(elem.getAttribute("target"));
                box2.setText(elem.getAttribute("amount"));
                box3.setText(elem.getAttribute("roll"));
                break;
            case 3:
                tag1.setText("Name od skill required");
                tag2.setText("Type of req");
                tag3.setText("Bonus / penality");
                tag4.setText("Not Used");
                box4.setEnabled(false);
                box1.setText(elem.getAttribute("name"));
                box2.setText(elem.getAttribute("type"));
                box3.setText(elem.getAttribute("bonus"));
                break;
        }
    }
