    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        switch (type) {
            case 0:
                e.setAttribute("name", box1.getText());
                e.setAttribute("base", box2.getText());
                e.setAttribute("onLevel", box3.getText());
                break;
            case 1:
                e.setAttribute("name", box1.getText());
                e.setAttribute("bonus", box2.getText());
                e.setAttribute("altSkill", box3.getText());
                e.setAttribute("altBonus", box4.getText());
                break;
            case 2:
                //if(box2.getText().equalsIgnoreCase("") ^ box3.getText().equalsIgnoreCase("")){
                e.setAttribute("target", box1.getText());
                e.setAttribute("amount", box2.getText());
                e.setAttribute("roll", box3.getText());
                //}
                break;
            case 3:
                e.setAttribute("name", box1.getText());
                e.setAttribute("type", box2.getText());
                e.setAttribute("bonus", box3.getText());
                break;
        }
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

